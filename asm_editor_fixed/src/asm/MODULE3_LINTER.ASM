ORG 100h
START:
MAIN:
    MOV  DX, OFFSET MSG_PROMPT
    CALL PRINT_STR
    MOV  AH, 0Ah
    MOV  DX, OFFSET INPUT_BUF
    INT  21h
    MOV  DX, OFFSET CRLF
    CALL PRINT_STR
    MOV  SI, OFFSET INPUT_BUF+1
    XOR  CH, CH
    MOV  CL, [SI]
    INC  SI
    MOV  DI, OFFSET LINE_BUF
    JCXZ MAIN
UC_LOOP:
    MOV  AL, [SI]
    CMP  AL, 61h
    JB   UC_OK2
    CMP  AL, 7Ah
    JA   UC_OK2
    SUB  AL, 20h
UC_OK2:
    MOV  [DI], AL
    INC  SI
    INC  DI
    LOOP UC_LOOP
    MOV  BYTE PTR [DI], 0
    MOV  SI, OFFSET LINE_BUF
    MOV  DI, OFFSET KW_EXIT
    CALL STR_CMP
    JZ   QUIT
    CALL LINT_LINE
    JMP  MAIN
QUIT:
    MOV  DX, OFFSET MSG_BYE
    CALL PRINT_STR
    MOV  AH, 4Ch
    INT  21h
LINT_LINE PROC
    MOV  SI, OFFSET LINE_BUF
    MOV  DI, OFFSET PAT_MOV_CS
    CALL CHECK_PREFIX
    JZ   ERR_MOV_CS
    MOV  SI, OFFSET LINE_BUF
    MOV  DI, OFFSET PAT_MOV_AX_AL
    CALL CHECK_PREFIX
    JZ   ERR_SIZE_MISM
    MOV  SI, OFFSET LINE_BUF
    CALL COUNT_BRACKETS
    CMP  AL, 2
    JGE  ERR_DOUBLE_MEM
    MOV  SI, OFFSET LINE_BUF
    MOV  DI, OFFSET PAT_PUSH_ONLY
    CALL STR_CMP
    JZ   ERR_PUSH_NO_OP
    MOV  DX, OFFSET MSG_OK
    CALL PRINT_STR
    RET
ERR_MOV_CS:
    MOV  DX, OFFSET MSG_ERR_CS
    CALL PRINT_STR
    RET
ERR_SIZE_MISM:
    MOV  DX, OFFSET MSG_ERR_SZ
    CALL PRINT_STR
    RET
ERR_DOUBLE_MEM:
    MOV  DX, OFFSET MSG_ERR_MEM
    CALL PRINT_STR
    RET
ERR_PUSH_NO_OP:
    MOV  DX, OFFSET MSG_ERR_PUSH
    CALL PRINT_STR
    RET
LINT_LINE ENDP
COUNT_BRACKETS PROC
    XOR  AL, AL
CB_L:
    MOV  BL, [SI]
    CMP  BL, 0
    JE   CB_DONE
    CMP  BL, '['
    JNE  CB_NEXT
    INC  AL
CB_NEXT:
    INC  SI
    JMP  CB_L
CB_DONE:
    RET
COUNT_BRACKETS ENDP
CHECK_PREFIX PROC
    PUSH SI
    PUSH DI
CPX:
    MOV  AL, [DI]
    CMP  AL, 0
    JE   CPX_OK
    MOV  BL, [SI]
    CMP  AL, BL
    JNE  CPX_NO
    INC  SI
    INC  DI
    JMP  CPX
CPX_OK:
    POP DI
    POP SI
    XOR AX,AX
    RET
CPX_NO:
    POP DI
    POP SI
    OR  AX,1
    RET
CHECK_PREFIX ENDP
STR_CMP PROC
SC:
    MOV AL,[SI]
    MOV BL,[DI]
    CMP AL,BL
    JNE SCN
    CMP AL,0
    JE  SCY
    INC SI
    INC DI
    JMP SC
SCY:
    XOR AX,AX
    RET
SCN:
    OR  AX,1
    RET
STR_CMP ENDP
PRINT_STR PROC
    MOV AH,09h
    INT 21h
    RET
PRINT_STR ENDP
INPUT_BUF     DB 40, 0, 41 DUP(0)
LINE_BUF      DB 42 DUP(0)
KW_EXIT       DB 'EXIT', 0
PAT_MOV_CS    DB 'MOV CS', 0
PAT_MOV_AX_AL DB 'MOV AX, AL', 0
PAT_PUSH_ONLY DB 'PUSH', 0
CRLF          DB 0Dh,0Ah,'$'
MSG_PROMPT    DB 0Dh,0Ah,'  Instruction: ','$'
MSG_OK        DB '  Result: OK - no error .',0Dh,0Ah,'$'
MSG_ERR_CS    DB '  ERROR : CS cannot be a destination register.',0Dh,0Ah,'$'
MSG_ERR_SZ    DB '  ERROR : Size mismatch - 16-bit dst, 8-bit src.',0Dh,0Ah,'$'
MSG_ERR_MEM   DB '  ERROR : MOV cannot have two memory operands.',0Dh,0Ah,'$'
MSG_ERR_PUSH  DB '  ERROR : PUSH requires an operand.',0Dh,0Ah,'$'
MSG_BYE       DB 0Dh,0Ah,' Finished!',0Dh,0Ah,'$'
END START
